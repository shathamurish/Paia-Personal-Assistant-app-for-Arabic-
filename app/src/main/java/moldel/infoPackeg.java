package moldel;

public class infoPackeg {
    String word="";
    String pakeg="";

    public infoPackeg(String word, String pakeg) {
        this.word = word;
        this.pakeg = pakeg;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public String getPakeg() {
        return pakeg;
    }

    public void setPakeg(String pakeg) {
        this.pakeg = pakeg;
    }
}
